public class SampleValidatorException extends Exception {
    
    public SampleValidatorException() {
        super();
    }
    
    public SampleValidatorException(String message) {
        super(message);
    }
    
    public SampleValidatorException(Throwable cause) {
        super(cause);
    }
    
    public SampleValidatorException(String message, Throwable cause) {
        super(message, cause);
    }
  
}
